/**
 * @enum UsergroupSortableField
 Enumeration of sortable fields for user groups
 */
export enum UsergroupSortableField {
  /** Sort by ID */
  ID = 'ID',
  /** Sort by name */
  NAME = 'NAME',
  /** Sort by description */
  DESCRIPTION = 'DESCRIPTION',
  /** Sort by created date */
  CREATED_DATE = 'CREATED_DATE',
  /** Sort by updated date */
  UPDATED_DATE = 'UPDATED_DATE',
  /** Sort by is active */
  IS_ACTIVE = 'IS_ACTIVE'
}