/**
 * @enum SortOrder
 Sort order enumeration for database queries
 */
export enum SortOrder {
  /** Ascending order (lowercase) */
  asc = "asc",
  /** Descending order (lowercase) */
  desc = "desc",
  /** Ascending order (uppercase) */
  ASC = "ASC",
  /** Descending order (uppercase) */
  DESC = "DESC",
}